package gym.management.Sessions;

/**
 * Defines the type of forum for a gym session.
 */
public enum ForumType {
    /**
     * Sessions for male participants only.
     */
    Male,

    /**
     * Sessions for female participants only.
     */
    Female,

    /**
     * Sessions designed for senior participants (e.g., age 65 and older).
     */
    Seniors,

    /**
     * Sessions open to all participants, regardless of gender or age.
     */
    All
}
